package Model;

/**
 *
 * @author Diana
 */
public class Presente {
    private int id;
    private String verbo;
    private String je;
    private String tu;
    private String il;
    private String nous;
    private String vous;
    private String ils;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getVerbo() {
        return verbo;
    }

    public void setVerbo(String verbo) {
        this.verbo = verbo;
    }

    public String getJe() {
        return je;
    }

    public void setJe(String je) {
        this.je = je;
    }

    public String getTu() {
        return tu;
    }

    public void setTu(String tu) {
        this.tu = tu;
    }

    public String getIl() {
        return il;
    }

    public void setIl(String il) {
        this.il = il;
    }

    public String getNous() {
        return nous;
    }

    public void setNous(String nous) {
        this.nous = nous;
    }

    public String getVous() {
        return vous;
    }

    public void setVous(String vous) {
        this.vous = vous;
    }

    public String getIls() {
        return ils;
    }

    public void setIls(String ils) {
        this.ils = ils;
    }


    
    
}
